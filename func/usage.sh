usage() {
    echo "Usage: ${0##*/} [--help|-h] [--version|-v] [--kernel|-k <version>] [arg]"
    echo ""
    echo "-h, --help        Display this help"
    echo "-v, --version     Display version and exit"
    echo "-k, --kernel      kernel version in format linux-<version>-gentoo"
    echo ""
    echo ""
}
